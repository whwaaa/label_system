package com.jumaojiang.pojo;

public class Style {
    private Integer styleId;

    private Byte size;

    private Byte intFabric;

    private Byte filler;

    private Byte guidePrice;

    public Integer getStyleId() {
        return styleId;
    }

    public void setStyleId(Integer styleId) {
        this.styleId = styleId;
    }

    public Byte getSize() {
        return size;
    }

    public void setSize(Byte size) {
        this.size = size;
    }

    public Byte getIntFabric() {
        return intFabric;
    }

    public void setIntFabric(Byte intFabric) {
        this.intFabric = intFabric;
    }

    public Byte getFiller() {
        return filler;
    }

    public void setFiller(Byte filler) {
        this.filler = filler;
    }

    public Byte getGuidePrice() {
        return guidePrice;
    }

    public void setGuidePrice(Byte guidePrice) {
        this.guidePrice = guidePrice;
    }
}